  `is_appoint_delivery` char(2) DEFAULT NULL COMMENT '是否预约',
  `appoint_delivery_status` varchar(55) DEFAULT NULL COMMENT '预约状态',
  `appoint_delivery_remark` varchar(255) DEFAULT NULL COMMENT '预约备注',
  `order_url` varchar(255) DEFAULT NULL COMMENT '商品网址',
  `order_website` varchar(255) DEFAULT NULL COMMENT '平台网址',
