  `DeliveryModel` varchar(50) DEFAULT NULL COMMENT '订单发货模式',
  `IsAllowSplit` char(2) DEFAULT NULL COMMENT '是否允许拆单',
  `ReceiverCompany` varchar(255) DEFAULT NULL COMMENT '收件方公司',
  `ReceiverName` varchar(255) DEFAULT NULL COMMENT '收件方名称',
  `ReceiverEmail` varchar(255) DEFAULT NULL COMMENT '收件方电子邮箱',
  `ReceiverZipCode` varchar(55) DEFAULT NULL COMMENT '收件方邮编',
  `ReceiverMobile` varchar(55) DEFAULT NULL COMMENT '收件方手机',
  `ReceiverPhone` varchar(55) DEFAULT NULL COMMENT '收件方电话',
  `CountryCode` char(5) DEFAULT NULL COMMENT '收件人国家代码',
  `ConsigneeFullname` varchar(255) DEFAULT NULL COMMENT '收件人全名',
  `ReceiverCountry` varchar(255) DEFAULT NULL COMMENT '收件方国家',
  `ReceiverProvince` varchar(255) DEFAULT NULL COMMENT '收件方省份',
  `ReceiverCity` varchar(255) DEFAULT NULL COMMENT '收件方城市',
  `ReceiverArea` varchar(255) DEFAULT NULL COMMENT '收件方区县',
  `ReceiverAddress` varchar(255) DEFAULT NULL COMMENT '收件方地址',
  `ConsigneeStreet` varchar(255) DEFAULT NULL COMMENT '所在街道',
  `ConsigneeDoorplate` varchar(255) DEFAULT NULL COMMENT '门牌号',
  `ReceiverIdType` varchar(255) DEFAULT NULL COMMENT '收件方证件类型',
  `ReceiverIdCard` varchar(255) DEFAULT NULL COMMENT '收件方证件号码',
  `OrderSenderInfo` text COMMENT '寄方信息',
  `order_detail` text COMMENT '整张订单的所有信息',
  `addinfo` text,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB  DEFAULT CHARSET=utf8 AUTO_INCREMENT=21 ;


